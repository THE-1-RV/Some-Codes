E = {0, 2, 4, 6, 8}
N = {1, 2, 3, 4, 5, 6}

union = E | N           
intersection = E & N       
difference = E - N       
s_difference = E ^ N      

print("Union of E and N is", union)
print("Intersection of E and N is", intersection)
print("Difference of E and N is", difference)
print("Symmetric Difference of E and N is", s_difference) 